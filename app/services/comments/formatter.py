def format_issue_success_comment(pr_url: str, attempts: int, tests_passed: bool) -> str:
    return (
        "micro-swe-agent completed this task.\n\n"
        f"- Status: success\n"
        f"- Pull Request: {pr_url}\n"
        f"- Attempts: {attempts}\n"
        f"- Tests passed: {'yes' if tests_passed else 'no'}\n"
    )


def format_issue_failure_comment(reason: str, attempts: int) -> str:
    return (
        "micro-swe-agent could not complete this task.\n\n"
        f"- Status: failed\n"
        f"- Attempts: {attempts}\n"
        f"- Failure reason: {reason}\n"
    )


def format_pr_body(
    issue_number: int,
    root_cause: str,
    changes: list[str],
    validation_summary: str,
) -> str:
    bullet_lines = "\n".join(f"- {change}" for change in changes) if changes else "- Minimal targeted patch"
    summary_line = (
        f"Fixes #{issue_number} by applying a minimal patch."
        if issue_number > 0
        else "Combines selected agent-generated changes into a conflict-aware integration patch."
    )
    return (
        "## Summary\n"
        f"{summary_line}\n\n"
        "## Root Cause\n"
        f"{root_cause}\n\n"
        "## Changes\n"
        f"{bullet_lines}\n\n"
        "## Validation\n"
        "- pytest -q\n"
        f"- {validation_summary}\n\n"
        "## Notes\n"
        "- generated by micro-swe-agent\n"
        "- requires human review\n"
    )
