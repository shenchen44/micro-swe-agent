from app.services.comments.formatter import format_pr_body


def test_pr_body_generation() -> None:
    body = format_pr_body(
        issue_number=17,
        root_cause="None values were passed into title formatting.",
        changes=["Guard against None input", "Add regression test"],
        validation_summary="2 tests passed",
    )
    assert "Fixes #17" in body
    assert "Guard against None input" in body
    assert "generated by micro-swe-agent" in body
